#include <unistd.h>

int	main(int argc, char **ar)
{
	int		i;
	char	*ptr;

	if (argc > 1)
	{
		i = argc - 1;
		while (i >= 1)
		{
			ptr = ar[i];
			while (*ptr)
				write(1, ptr++, 1);
			write(1, "\n", 1);
			i--;
		}
	}
}
